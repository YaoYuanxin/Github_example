def add(x,y):
  if (type(x) == int and type(y) == int):
    return x + y
  else:
    print("Need integer inputs for this function")
    return None
  
  
def mutiply(x,y):
  if (type(x) == int and type(y) == int):
    return x*y
  else:
    print("Need integers inputs for this function also")
    return None